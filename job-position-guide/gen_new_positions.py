    lines.append('        "fit_for": [%s],' % ", ".join('"%s"' % x for x in meta["fitFor"]))
    lines.append('        "hard_skills": [%s],' % ", ".join('"%s"' % x for x in meta["hardSkills"]))
    lines.append('        "soft_skills": [%s],' % ", ".join('"%s"' % x for x in meta["softSkills"]))
    ind = ", ".join('"%s": "%s"' % (k, v) for k, v in meta["industryNotes"].items())
    lines.append('        "industry_notes": {%s},' % ind)
    lines.append('        "taboos": [%s],' % ", ".join('"%s"' % x for x in meta["taboos"]))
    lines.append('        "holland_code": "%s"' % meta["holland_code"])
    lines.append('    }')
    return "\n".join(lines)

