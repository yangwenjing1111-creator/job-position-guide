# ---------- 生成 JS ----------
def q_daily(s, opts):
    ol = ",\n".join(f'        {{ key: "{k}", text: "{t}", score: {{ D1: {sc} }} }}' for k,t,sc in opts)
    return (f'      {{ type: "日常", scenario: "{s}", options: [\n'
            f'{ol}\n'
            f'      ] }}')

def q_pressure(s, opts, ref):
    ol = ",\n".join(f'        {{ key: "{k}", text: "{t}", score: {{ D2: {sc} }} }}' for k,t,sc in opts)
    return (f'      {{ type: "压力", scenario: "{s}", options: [\n'
            f'{ol}\n'
            f'      ], reference: "{ref}" }}')

def q_dev(s, opts):
    ol = ",\n".join(f'        {{ key: "{k}", text: "{t}", score: {{ D3: {sc} }} }}' for k,t,sc in opts)
    return (f'      {{ type: "发展", scenario: "{s}", options: [\n'
            f'{ol}\n'
            f'      ] }}')

def build_array(sets):
    items = [q_daily(s,o) for s,o in sets["daily"]]
    items += [q_pressure(s,o,r) for s,o,r in sets["pressure"]]
    items += [q_dev(s,o) for s,o in sets["dev"]]
    body = ",\n".join(items)
    return "[\n" + body + "\n    ]"

