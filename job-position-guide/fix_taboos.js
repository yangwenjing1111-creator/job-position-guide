// ---- 改 data.js ----
global.window = {};
require('./data.js');
const P = global.window.POSITIONS;
let changed = 0;
const missing = [];
for (const p of P) {
  if (map[p.id]) { p.taboos = map[p.id]; changed++; }
  else missing.push(p.id);
}
if (missing.length) { console.error('data.js 缺映射:', missing.join(',')); process.exit(1); }

const raw = fs.readFileSync('./data.js', 'utf8');
const start = raw.indexOf('window.POSITIONS');
const eq = raw.indexOf('[', start);
let depth = 0, end = -1;
for (let i = eq; i < raw.length; i++) {
  if (raw[i] === '[') depth++;
  else if (raw[i] === ']') { depth--; if (depth === 0) { end = i; break; } }
}
const head = raw.slice(0, eq);
const tail = raw.slice(end + 1);
fs.writeFileSync('./data.js', head + JSON.stringify(P, null, 2) + tail);
console.log('data.js 已更新', changed, '岗 taboos');

// 导出映射供 init_db.py 使用
fs.writeFileSync('./taboos_map.json', JSON.stringify(map, null, 2));
console.log('已导出 taboos_map.json');
